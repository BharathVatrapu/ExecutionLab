    public void btnNextActionPerformed(){
        boolean flag=true;
        projectType=Generic.getSelectedRadioButtonText(bgprojectType);
        System.out.println("projectType::"+projectType);

        if(fieldValidation()){
            System.out.println("table exist::"+SQLLite.tableExists(GlobalConstants.tblPROJECTS));
            if(SQLLite.tableExists(GlobalConstants.tblPROJECTS)){
                if(SQLLite.rowExists(GlobalConstants.tblPROJECTS,"PROJECT_NAME",edtProjectName.getText())){
                    JOptionPane.showMessageDialog(null,edtProjectName.getText()+"Already Exists");
                    edtProjectName.setBackground(new java.awt.Color(255, 255, 0));
                    flag=false;
                } else  {
                    SQLLite.insertProjectsTable(edtAccountName.getText(), edtProjectName.getText(), rbSEMaven.getText(), edtProjectPath.getText());

                }
            } else {
                SQLLite.createTable(GlobalConstants.PROJECTS_SQL_TABLE);
                SQLLite.insertProjectsTable(edtAccountName.getText(), edtProjectName.getText(), rbSEMaven.getText(), edtProjectPath.getText());
            }

        } else {
            flag=false;
        }

        if(flag){
            GlobalConstants.Project_Name = edtProjectName.getText();
            Main.projectParametersSetup_frame();
        }
    }
