    public boolean fieldValidation(){
        boolean flag=true;
        if(edtAccountName.getText().isEmpty() || edtProjectName.getText().isEmpty() || edtProjectPath.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Enter All Fields");
            flag =  false;
        }
        return flag;
    }




